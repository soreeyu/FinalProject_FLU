package com.flu.profile;


public class PortFolioImg {
	private int pfNum;
	private String Fimage;
	private String Oimage;
	private String arrage;
	private String contents;
	
	
	public int getPfNum() {
		return pfNum;
	}
	public void setPfNum(int pfNum) {
		this.pfNum = pfNum;
	}
	public String getFimage() {
		return Fimage;
	}
	public void setFimage(String fimage) {
		Fimage = fimage;
	}
	public String getOimage() {
		return Oimage;
	}
	public void setOimage(String oimage) {
		Oimage = oimage;
	}
	public String getArrage() {
		return arrage;
	}
	public void setArrage(String arrage) {
		this.arrage = arrage;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}

	
	
}
